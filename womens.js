 function AddToCart(id){
  //console.log(id);
  var productsInCart=JSON.parse(localStorage.getItem("CartItems")) || [];

  var product= womensData.filter(function(elm){
    return elm.id==id;
  })

  var productExist= productsInCart.filter(function(elm){
    return elm.id==id;
  });
  if(productExist.length==0){
    productsInCart.push(product[0]);
    localStorage.setItem("CartItems",JSON.stringify(productsInCart));
  }else{
    alert("Product Already in the Cart");
  }

  var cartCount = productsInCart.length;

  localStorage.setItem("cartofCount",cartCount);

  document.getElementById("cartCount").innerText=cartCount;




}


