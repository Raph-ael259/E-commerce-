 function displayProducts(Data){
      Data.map(function(data){
        var ProductBox=document.createElement("div");
        ProductBox.setAttribute('id',data.id);

        var pImg= document.createElement("img");
        pImg.setAttribute('src',data.image_url);


        var pName= document.createElement("h5");
        pName.innerText=data.name;

        var priceBox= document.createElement("div");
        priceBox.setAttribute('class','price');

        var price= document.createElement("p");
        price.innerText="₹"+data.price;
        var strikeprice= document.createElement("p");
        strikeprice.innerText="₹"+data.strikedoffprice;
        priceBox.append(price,strikeprice);
        
        var cartBtn=document.createElement("button");
        cartBtn.innerText="Add To Cart";

        cartBtn.addEventListener("click",function(){
          AddToCart(data.id);
        });

        ProductBox.append(pImg,pName,priceBox,cartBtn);

        document.getElementById("PBox").append(ProductBox);
          
      })
 }


